// =, ==, ===, !=, !==

// = --> Assignement Operator
// == --> Loose Comparsion Operator(Sikh vs Hindu)
// === --> Strict Comparsion Operator(Hindu vs Hindu)

// <, >, <=, >= , !=, !==

console.log(3 > 4); // false
console.log(3 < 4); // true
console.log(3 >= 4); // false
console.log(3 <= 4); // true
console.log(3 != 4); // true
console.log(3 !== 4); // true

