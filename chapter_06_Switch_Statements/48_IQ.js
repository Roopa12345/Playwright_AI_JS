let testScore = 85;

switch (true) {
    case (testScore >= 95):
        console.log("Grade A");
        break;
    case (testScore >= 85):
        console.log("Grade B");
        break;
    case (testScore >= 75):
        console.log("Grade C");
        break;
    case (testScore >= 65):
        console.log("Grade D");
        break;
    default:
        console.log("Grade F");
}
