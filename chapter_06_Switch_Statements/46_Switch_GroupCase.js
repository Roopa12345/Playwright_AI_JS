let browser = "Edge";

switch (browser) {
    case "Edge":
    case "Safari":
    case "Chrome":
        console.log("Launching Chrome");
        break;
    case "Firefox":
        console.log("Launching Firefox");
        break;
    case "Edge":
        console.log("Launching Edge");
        break;
    default:
        console.log("Invalid browser");
}
