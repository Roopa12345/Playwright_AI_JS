//Increment Operator
// ++ -> increase the value 1
// Pre -> ()
let a2 = 10;
let b2 = ++a2;
console.log(b2);
console.log(a2);

//Increment Operator
// -- -> increase the value 1
// Post -> ()
let a3 = 10;
let b3 = a3++;
console.log(b3);
console.log(a3);
